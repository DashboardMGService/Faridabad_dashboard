// Helper functions

// Get all service advisors for a specific month and year
export const getMonthlyServiceAdvisors = (
  year: number,
  month: 'jan' | 'feb' | 'mar' | 'apr' | 'may' | 'jun' | 'jul' | 'aug' | 'sep' | 'oct' | 'nov' | 'dec',
  type: 'mech' | 'body'
): ServiceAdvisorData[] => {
  if (type === 'mech') {
    switch(year) {
      case 2024:
        return serviceAdvisorMech2024[month];
      case 2025:
        return serviceAdvisorMech2025[month];
      case 2026:
        return serviceAdvisorMech2026[month];
      default:
        return [];
    }
  } else {
    switch(year) {
      case 2024:
        return serviceAdvisorBody2024[month];
      case 2025:
        return serviceAdvisorBody2025[month];
      case 2026:
        return serviceAdvisorBody2026[month];
      default:
        return [];
    }
  }
};

// Get combined data (mechanical + bodyshop) for a specific month and year
export const getCombinedMonthlyData = (
  year: number,
  month: 'jan' | 'feb' | 'mar' | 'apr' | 'may' | 'jun' | 'jul' | 'aug' | 'sep' | 'oct' | 'nov' | 'dec'
): ServiceAdvisorData[] => {
  const mechData = getMonthlyServiceAdvisors(year, month, 'mech');
  const bodyData = getMonthlyServiceAdvisors(year, month, 'body');
  
  return [...mechData, ...bodyData];
};

// Calculate total revenue for a service advisor
export const calculateTotalRevenue = (advisor: ServiceAdvisorData): number => {
  return advisor.vas + advisor.lab + advisor.acces + advisor.lub + advisor.bat + advisor.tyre + advisor.parts;
};

// Get top service advisors by revenue for a specific month and year
export const getTopServiceAdvisors = (
  year: number,
  month: 'jan' | 'feb' | 'mar' | 'apr' | 'may' | 'jun' | 'jul' | 'aug' | 'sep' | 'oct' | 'nov' | 'dec',
  count: number = 10
): ServiceAdvisorData[] => {
  const combinedData = getCombinedMonthlyData(year, month);
  
  return [...combinedData]
    .sort((a, b) => calculateTotalRevenue(b) - calculateTotalRevenue(a))
    .slice(0, count);
};

// Get year-on-year comparison data for a specific month
export const getYearOnYearComparison = (
  year1: number,
  year2: number,
  month: 'jan' | 'feb' | 'mar' | 'apr' | 'may' | 'jun' | 'jul' | 'aug' | 'sep' | 'oct' | 'nov' | 'dec'
) => {
  const data1 = getCombinedMonthlyData(year1, month);
  const data2 = getCombinedMonthlyData(year2, month);
  
  // Calculate totals for each year
  const totals1 = data1.reduce(
    (acc, advisor) => {
      acc.throughput += advisor.throughput;
      acc.vas += advisor.vas;
      acc.lab += advisor.lab;
      acc.acces += advisor.acces;
      acc.lub += advisor.lub;
      acc.bat += advisor.bat;
      acc.tyre += advisor.tyre;
      acc.parts += advisor.parts;
      acc.totalRevenue += calculateTotalRevenue(advisor);
      return acc;
    },
    { 
      throughput: 0, vas: 0, lab: 0, acces: 0, lub: 0, bat: 0, 
      tyre: 0, parts: 0, totalRevenue: 0 
    }
  );
  
  const totals2 = data2.reduce(
    (acc, advisor) => {
      acc.throughput += advisor.throughput;
      acc.vas += advisor.vas;
      acc.lab += advisor.lab;
      acc.acces += advisor.acces;
      acc.lub += advisor.lub;
      acc.bat += advisor.bat;
      acc.tyre += advisor.tyre;
      acc.parts += advisor.parts;
      acc.totalRevenue += calculateTotalRevenue(advisor);
      return acc;
    },
    { 
      throughput: 0, vas: 0, lab: 0, acces: 0, lub: 0, bat: 0, 
      tyre: 0, parts: 0, totalRevenue: 0 
    }
  );
  
  // Calculate percentage changes
  const percentageChanges = {
    throughput: totals1.throughput > 0 ? ((totals2.throughput - totals1.throughput) / totals1.throughput) * 100 : 0,
    vas: totals1.vas > 0 ? ((totals2.vas - totals1.vas) / totals1.vas) * 100 : 0,
    lab: totals1.lab > 0 ? ((totals2.lab - totals1.lab) / totals1.lab) * 100 : 0,
    acces: totals1.acces > 0 ? ((totals2.acces - totals1.acces) / totals1.acces) * 100 : 0,
    lub: totals1.lub > 0 ? ((totals2.lub - totals1.lub) / totals1.lub) * 100 : 0,
    bat: totals1.bat > 0 ? ((totals2.bat - totals1.bat) / totals1.bat) * 100 : 0,
    tyre: totals1.tyre > 0 ? ((totals2.tyre - totals1.tyre) / totals1.tyre) * 100 : 0,
    parts: totals1.parts > 0 ? ((totals2.parts - totals1.parts) / totals1.parts) * 100 : 0,
    totalRevenue: totals1.totalRevenue > 0 ? ((totals2.totalRevenue - totals1.totalRevenue) / totals1.totalRevenue) * 100 : 0
  };
  
  return {
    month,
    [`data${year1}`]: totals1,
    [`data${year2}`]: totals2,
    percentageChanges
  };
};
